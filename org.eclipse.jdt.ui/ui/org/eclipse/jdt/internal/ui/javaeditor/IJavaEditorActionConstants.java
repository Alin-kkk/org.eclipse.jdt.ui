package org.eclipse.jdt.internal.ui.javaeditor;

/**
 * Defines action IDs for JavaEditor actions.
 */
public interface IJavaEditorActionConstants {

	/**
	 * Edit menu: Name of the action to open on selection.
	 */
	public static final String OPEN_SELECTION= "openSelection";

	/**
	 * Edit menu: Name of the action to open type hierarchy.
	 */
	public static final String OPEN_TYPE_HIERARCHY= "openTypeHierarchy";

	/**
	 * Edit menu: Name of the action to toggle presentation.
	 */
	public static final String TOGGLE_PRESENTATION= "togglePresentation";

	/**
	 * Edit menu: Name of the action to toggle text hover.
	 */
	public static final String TOGGLE_TEXT_HOVER= "toggleTextHover";

	/**
	 * Edit menu: Name of the action to goto next error.
	 */
	public static final String NEXT_ERROR= "nextError";

	/**
	 * Edit menu: Name of the action to goto previous error.
	 */
	public static final String PREVIOUS_ERROR= "previousError";

	/**
	 * Edit menu: Name of the action to show javadoc.
	 */
	public static final String SHOW_JAVADOC= "showJavadoc";

	/**
	 * Edit menu: Name of the action to open external javadoc.
	 */
	public static final String OPEN_JAVADOC= "openJavadoc";
	
}
